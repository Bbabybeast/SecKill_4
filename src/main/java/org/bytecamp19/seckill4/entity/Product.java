public class Product {
    private int pid;
    private int count;
    private int price;
    private String detail;

    @Override
    public String toString() {
        return "Product{" +
                "pid=" + pid +
                ", count=" + count +
                ", price=" + price +
                ", detail='" + detail + '\'' +
                '}';
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
